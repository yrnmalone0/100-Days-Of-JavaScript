// getting today's date 
const todaysDay_time = new Date();
console.log(todaysDay_time);

// What is the year today?
console.log(todaysDay_time.getFullYear());

// What is the month today as a number?
console.log(todaysDay_time.getMonth());

// What is the date today?
console.log(todaysDay_time.getDate());

// What is the day today as a number?
console.log(todaysDay_time.getDay());

// What is the hours now?
console.log(todaysDay_time.getHours());

// What is the minutes now?
console.log(todaysDay_time.getMinutes());

// Find out the numbers of seconds elapsed from January 1, 1970 to now.
console.log(todaysDay_time.getTime());